#include "../../incl/loop.h"

int	ft_isspace(char a)
{
	if (a == ' ' || a == '\t' || a == '\n')
		return (1);
	return (0);
}

int	is_special_char(char c)
{
	return (c == '|' || c == '<' || c == '>' || c == '&');
}

int is_quote(char c)
{
	return (c == '"' || c == '\'');
}